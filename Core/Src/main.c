  HAL_TIM_Base_Start(&htim2);

  HAL_TIM_Base_Start(&htim3);               //Initialize stm32 timer 3
  HAL_TIM_PWM_Start(&htim3,TIM_CHANNEL_3);  //PB0 Start pwm motor 100% duty cycle


  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */


